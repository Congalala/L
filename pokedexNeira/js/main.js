const listaPokemon = document.querySelector("#listaPokemon");
const modal = document.getElementById("mainModal");

for (let i = 0; i < orquideas.length; i++) {
    mostrarPokemon(orquideas[i]);
}

function mostrarPokemon(orquidea) {
    const div = document.createElement("div");
    div.classList.add("pokemon");
    div.innerHTML = `
        <p class="pokemon-id-back">#${orquidea.id}</p>
        <div class="pokemon-imagen">
            <img class="imgOrquidea" src="${orquidea.imagen}" alt="${orquidea.nombre}">
            <button class="btn btn-ver" id="${orquidea.id}">ver</button>
        </div>
        <div class="pokemon-info">
            <div class="nombre-contenedor">
                <p class="pokemon-id">#${orquidea.id}</p>
                <em><h2 class="pokemon-nombre">${orquidea.nombre}</h2></em>
            </div>
            <div class="pokemon-tipos">
                <p class="electric tipo">Electrico</p>
                <p class="fighting tipo">Lucha</p>
            </div>
            <div class="pokemon-stats">
                <p class="stat">Región Biogeográfica</p>
                <p class="stat-text">${orquidea.region}</p>
                <p class="stat">Elevación</p>
                <p class="stat-text">${orquidea.elevacion}</p>
            </div>
        </div>
    `;
    listaPokemon.append(div);
}

